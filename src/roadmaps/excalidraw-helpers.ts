export type ExcalidrawElement = Record<string, unknown>;

export function excalidrawText(id: string, x: number, y: number, text: string, fontSize = 22, width = 240): ExcalidrawElement {
  const lineCount = text.split('\n').length;
  return {
    id,
    type: 'text',
    x,
    y,
    width,
    height: Math.ceil(fontSize * 1.25 * lineCount),
    angle: 0,
    strokeColor: '#1f2937',
    backgroundColor: 'transparent',
    fillStyle: 'solid',
    strokeWidth: 1,
    strokeStyle: 'solid',
    roughness: 1,
    opacity: 100,
    groupIds: [],
    frameId: null,
    roundness: null,
    seed: id.length * 97,
    version: 1,
    versionNonce: id.length * 101,
    isDeleted: false,
    boundElements: null,
    updated: 1,
    link: null,
    locked: false,
    text,
    fontSize,
    fontFamily: 5,
    textAlign: 'center',
    verticalAlign: 'top',
    containerId: null,
    originalText: text,
    lineHeight: 1.25,
  };
}

