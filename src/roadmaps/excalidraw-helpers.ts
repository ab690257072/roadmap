export function excalidrawLabeledBox(
  id: string,
  x: number,
  y: number,
  text: string,
  fontSize = 18,
  backgroundColor = '#e7f5ff',
): ExcalidrawElement[] {
  const lines = text.split('\n');
  const lineCount = lines.length;
  const maxChars = Math.max(...lines.map(l => l.length));
  const estimatedTextWidth = Math.max(maxChars * fontSize * 0.65 + 16, 60);
  const textHeight = Math.ceil(fontSize * 1.25 * lineCount);
  const boxId = `${id}-box`;
  const textId = `${id}-text`;

  return [
    {
      id: textId,
      type: 'text',
      x: x + 10,
      y: y + 8,
      width: estimatedTextWidth,
      height: textHeight,
      angle: 0,
      strokeColor: '#1f2937',
      backgroundColor: 'transparent',
      fillStyle: 'solid',
      strokeWidth: 1,
      strokeStyle: 'solid',
      roughness: 1,
      opacity: 100,
      groupIds: [],
      frameId: null,
      roundness: null,
      seed: textId.length * 97,
      version: 1,
      versionNonce: textId.length * 101,
      isDeleted: false,
      boundElements: null,
      updated: 1,
      link: null,
      locked: false,
      text,
      fontSize,
      fontFamily: 5,
      textAlign: 'center',
      verticalAlign: 'middle',
      containerId: boxId,
      originalText: text,
      lineHeight: 1.25,
    },
    {
      id: boxId,
      type: 'rectangle',
      x,
      y,
      width: estimatedTextWidth + 24,
      height: textHeight + 18,
      angle: 0,
      strokeColor: '#334155',
      backgroundColor,
      fillStyle: 'solid',
      strokeWidth: 2,
      strokeStyle: 'solid',
      roughness: 1,
      opacity: 100,
      groupIds: [],
      frameId: null,
      roundness: { type: 3 },
      seed: boxId.length * 89,
      version: 1,
      versionNonce: boxId.length * 103,
      isDeleted: false,
      boundElements: [{ id: textId, type: 'text' }],
      updated: 1,
      link: null,
      locked: false,
    },
  ];
}

